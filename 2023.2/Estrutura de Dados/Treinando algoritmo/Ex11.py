# Escreva um programa que solicite ao usuário dois números e troque o valor das variáveis entre si,
# ou seja, o valor da primeira variável passa para a segunda e vice-versa.

num1 = int(input('Digite o primeiro número: '))
num2 = int(input('Digite o segundo número: '))

numero1 = num2
numero2 = num1

print('O numero um agora é: ', numero1, 'E o numero dois agora é: ', numero2)